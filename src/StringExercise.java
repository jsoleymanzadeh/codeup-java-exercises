public class StringExercise {
    public static void main(String[] args) {
        String string1 = "We don't need no education";
        System.out.printf("%s\n%s\n", string1, string1.replace("education", "thought control"));
        String string2 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(string2);
        String string3 = "In windows, the main drive is usually C:\\";
        System.out.println(string3);
        String string4 = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\";
        System.out.println(string4);
    }
}